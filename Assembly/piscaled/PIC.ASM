#INCLUDE <P16F628A.INC>
ORG			0x00
			BSF		STATUS,RP0	;SELECIONA BANCO 1	
			MOVLW	0x00		;PORTA SAIDA
			MOVWF	TRISA		;CONF PORTA
			MOVLW	0x00		;PORTB SAIDA		
			MOVWF	TRISB		;CONF PORTB
			BCF		STATUS,RP0	;SELECIONA BANCO 0
			
INICIO
			BSF		PORTB,0		;SETA BIT
			CALL	DELAY		;GASTA TEMPO
			BCF		PORTB,0		;RESETA BIT	
			CALL	DELAY		;GASTA TEMPO
			GOTO	INICIO		;REINICIA
			
DELAY
			MOVLW	0x05		;VALOR
			MOVWF	0x20		;CONTADOR
			
VOLTA

			DECFSZ	0x20		;DECREMENTA E SALTA
			GOTO	VOLTA		
			RETURN				; RETORNA DA CHAMADA
			
END								;FIM

